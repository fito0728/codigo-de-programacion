# Definimos dos variables numéricas
a = 10
b = 4

# Multiplicación
print('La primera variable es:', a)
print('El signo de la operación es: * ')
print('La segunda variable es:', b)
c = a * b  # Realizamos la multiplicación
print('El resultado es:', c)
print(type(c))  # Mostramos el tipo de dato del resultado

# División
print('La primera variable es:', a)
print('El signo de la operación es: / ')
print('La segunda variable es:', b)
c = a / b  # Realizamos la división
print('El resultado es:', c)
print(type(c))  # Mostramos el tipo de dato del resultado

# Suma
print('La primera variable es:', a)
print('El signo de la operación es: + ')
print('La segunda variable es:', b)
c = a + b  # Realizamos la suma
print('El resultado es:', c)
print(type(c))  # Mostramos el tipo de dato del resultado

# Resta
print('La primera variable es:', a)
print('El signo de la operación es: - ')
print('La segunda variable es:', b)
c = a - b  # Realizamos la resta
print('El resultado es:', c)
